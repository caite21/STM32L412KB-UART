int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_USART2_UART_Init();
  /* USER CODE BEGIN 2 */

  /* BUG WARNING ABOVE: DMA init must be before UART init.
   * The STM32CubeIDE Device Configuration Tool will set them in
   * the wrong order every time. */

  /* Fill data with ASCII chars: first half is '!' to '{' on new lines*/
  uint32_t j = 0;
  for (uint32_t i = 0; i < sizeof(TxData)/2; i++) {
	  j = i % 93;
	  // new line after each pattern and at end of all data
	  if (j == 91 || i == sizeof(TxData)/2-2) {
		  // '/r'
		  data[i] = 13;
		  // Also fill first half of transmit buffer
		  TxData[i] = 13;
	  } else if (j == 92 || i == sizeof(TxData)/2-1) {
		  // '/n'
		  data[i] = 10;
		  TxData[i] = 10;
	  } else {
		  data[i] = 33 + j;
		  TxData[i] = 33 + j;
	  }
  }

  /* Second half is '}' to '!' on new lines */
  for (uint32_t i = sizeof(TxData)/2; i < sizeof(TxData); i++) {
	  j = i % 93;
	  // new line after each pattern and at end of all data
	  if (j == 91 || i == sizeof(TxData)-2) {
		  // '/r'
		  data[i] = 13;
	  } else if (j == 92 || i == sizeof(TxData)-1) {
		  // '/n'
		  data[i] = 10;
	  } else {
		  data[i] = 123 - j;
	  }
  }

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (countloop < 20)
  {
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */

	// Normal DMA transmit
	if (isSent == 1) {
	  HAL_UART_Transmit_DMA(&huart2, TxData, sizeof(TxData));
	  isSent = 0;
	}

	HAL_GPIO_TogglePin(GPIOB, GPIO_PIN_3);
	HAL_Delay(300);
	countloop++;
  }
  /* USER CODE END 3 */
}

